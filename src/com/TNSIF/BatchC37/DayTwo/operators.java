package com.TNSIF.BatchC37.DayTwo;

public class operators {

	public static void main(String[] args)
	{
	//concatenation	
		int	a=10;
		int	b=20;
		int	x=10;
		
		System.out.println("a	and	b	value	before	the	operations:"+a+""+b);
		
		//increament	operator
		++a;
		int	c=++a+b;
		System.out.println("c	value:"+c);
		
		//ternary	operator
		x=(10==x)?1:0;
				
		System.out.println(x);
		
		
		
	}

}
