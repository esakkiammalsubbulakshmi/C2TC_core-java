package com.TNSIF.BachC7.DayOne;

public class LoopingStatement {

	public static void main(String[] args) {
		//for	loop
				int	i;
				for	(i=10;i<=20;i++)
				{
					System.out.println(i);
				}
				
				System.out.println();
				System.out.println();
				
				
				//while	loop
						int	j=10;
						while(j<=20)
						{
							System.out.println(j);
							j++;
						}
						
						System.out.println();
						System.out.println();
						
						
				//Do	while
						int	k=10;
						do
						{
							System.out.println(k);
							k++;
						}while(k<=20);
	}

}
